export const initDatabase = async () => {
  const db = await SQLite.openDatabaseAsync('legalCodes.db');
  
  await db.withTransactionAsync(async () => {
    // Создаём таблицу
    await db.execAsync(`
      CREATE TABLE IF NOT EXISTS codes (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        code_name TEXT NOT NULL,
        article_number TEXT NOT NULL,
        title TEXT NOT NULL,
        text TEXT NOT NULL,
        part TEXT,
        updated_at TEXT DEFAULT CURRENT_TIMESTAMP
      );
      CREATE INDEX IF NOT EXISTS idx_search ON codes(code_name, article_number, title, text);
    `);

    // Проверяем, пуста ли база
    const result = await db.getFirstAsync('SELECT COUNT(*) as count FROM codes');
    
    if (result.count === 0) {
      // Заполняем данными
      for (const item of LEGAL_DATA) {
        await db.runAsync(
          `INSERT INTO codes (code_name, article_number, title, text, part) VALUES (?, ?, ?, ?, ?)`,
          [item.code_name, item.article_number, item.title, item.text, item.part]
        );
      }
    }
  });
  
  return db;
};